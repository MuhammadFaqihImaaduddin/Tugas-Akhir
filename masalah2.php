            <p><h4>Cara Mengeluarkan Air dari Speaker HP</h4>
              Speaker HP yang kemasukan air bisa membuat speaker tidak berfungsi secara maksimal. 
              Oleh karena itu, jika secara tidak sengaja HP anda kemasukan air segera lakukan 
              tindakan agar tidak menyebabkan kerusakan yang parah.  Ada banyak cara mengeluarkan air 
              dari speaker smartphone yang bisa dilakukan baik untuk tipe HP baterai tanam maupun tipe HP 
              biasa. Berikut ini beberapa cara bisa anda lakukan untuk mengeluarkan air 
              dari speaker HP. <br><br>

              1. Cek Pengaturan Volume HP
              Cara mengeluarkan air dari speaker HP yang pertama adalah dengan mengecek terlebih dahulu 
              pengaturan volume HP. anda perlu memastikan apakah suara yang dikeluarkan oleh speaker masih 
              normal atau tidak. Bisa jadi masalah disebabkan oleh pengaturan volume yang keliru, bukan 
              karena terkena air. <br><br>

              2. Lakukan Tes pada Speaker HP
              Jika cara pertama belum berhasil, anda bisa lakukan cara selanjutnya yaitu dengan melakukan 
              tes pada speaker HP. Caranya adalah gunakan kode pengecekan atau kode informasi hardware. 
              Cara ini bertujuan untuk mengetahui apakah speaker mengalami kerusakan atau tidak sebelum 
              melakukan tindakan pembongkaran komponen speaker. <br><br>

              3. Bersihkan Speaker HP yang Terkena Air
              Jika masih tertinggal sisa air pada permukaan speaker HP, maka segera bersihkan agar air 
              tidak masuk ke dalam. Cara mengeluarkan air dari speaker HP bisa menggunakan jarum atau 
              sejenisnya. Dengan membersihkan sisa air yang menempel, biasanya akan membantu menghilangkan 
              suara kresek-kresek dari speaker. <br><br>

              4. Keringkan HP Menggunakan Hairdryer
              Selain bisa digunakan untuk mengeringkan rambut, hairdryer ternyata juga bisa dimanfaatkan 
              untuk mengeluarkan air dari speaker HP Iphone 7 plus atau HP jenis lainnya. Angin panas yang 
              dikeluarkan oleh hairdryer bisa membuat embun air yang menempel pada speaker menjadi kering.
              <br><br>
              Cara mengeringkan speaker HP dengan hairdryer cukup mudah. anda hanya perlu menyalakan 
              hair dryer dengan mode panas paling rendah kemudian arahkan ke speaker HP. Jika HP anda tipe 
              removable battery, sebaiknya lepas casing dan baterainya agar proses pengeringan lebih cepat 
              dan maksimal. <br><br>

              5. Keringkan HP di Media Beras
              Mengeringkan HP pada media beras juga bisa anda coba sebagai cara mengeluarkan air dari 
              speaker HP jika tercebur ke dalam air secara sepenuhnya. Beras memiliki sifat yang mampu 
              menyerap air dengan sangat baik sehingga bisa mengeringkan komponen HP yang lembab termasuk 
              speaker. <br><br>

              Untuk melakukan cara ini sangat mudah. anda hanya perlu menyiapkan beras ke dalam mangkuk 
              atau wadah kemudian kubur HP di dalamnya. Jika ingin hasil lebih maksimal, anda bisa melepas 
              komponen HP terlebih dahulu kemudian diamkan 2 sampai 3 hari. <br><br>

              6. Keringkan HP Menggunakan Kipas Angin
              Jika tidak memiliki hairdryer, anda bisa memanfaatkan kipas angin untuk mengeringkan HP 
              yang terkena air. Sebelum dikeringkan, pastikan semua komponen yang bisa dilepas anda copot 
              kemudian letakkan di depan kipas angin. Arahkan kipas angin agar fokus ke bagian speaker HP 
              supaya pengeringan lebih maksimal. Lakukan cara ini kurang lebih selama 5 jam sampai air di 
              dalam speaker HP benar-benar kering.</p>
